--Procedimiento para mostrar en lista, los productos que seran reubicados
--por exceder el peso máximo.
CREATE OR REPLACE PROCEDURE Productos_Reubicacion(p_PesoMaximo IN NUMBER)
AS
    --Se declara el cursor.
    CURSOR c_productos_grandes IS
        SELECT SKU,NOMBRE,PESO
        FROM PRODUCTO
        WHERE PESO > p_PesoMaximo
        ORDER BY PESO DESC;
            
    --Se declaran algunas variables de registro para el cursor.
    r_producto c_productos_grandes%ROWTYPE;
    
    v_contador INT:=0;
BEGIN
    DBMS_OUTPUT.PUT_LINE('Lista de los productos con peso superior a ' || p_PesoMaximo || ' kg a reubicar:');
    DBMS_OUTPUT.PUT_LINE('----------------------------------------------------');

    --Se abre el cursor.
    OPEN c_productos_grandes;
    
    --Se inicia el bucle para recorrer los resultados del cursor.
    LOOP
        --Obtiene la siguiente fila del cursor.
        FETCH c_productos_grandes INTO r_producto;
        
        --Para salir del bucle si ya no hay más filas.
        EXIT WHEN c_productos_grandes%NOTFOUND;
        
        --Se muestra en pantalla la información del producto.
        DBMS_OUTPUT.PUT_LINE('SKU: ' || r_producto.SKU || 
                             ' | Producto: ' || r_producto.NOMBRE ||
                             ' | Peso: ' || r_producto.PESO || ' kg.');
        
        v_contador := v_contador + 1;
    END LOOP;
    
    --Se cierra el cursor.
    CLOSE c_productos_grandes;
END Productos_Reubicacion;
/


EXEC Productos_Reubicacion(p_PesoMaximo => 1);




--TRIGGERS
--1.- Previene que cualquier empleado con un rol diferente a 'OPERATIVO' pueda 
--insertar o actualizar registros en la tabla ORDEN, levantando un error 
--específico en caso de violación.
CREATE OR REPLACE TRIGGER VALIDAR_OPERATIVO
BEFORE INSERT OR UPDATE ON ORDEN
FOR EACH ROW
DECLARE
    v_tipo_empleado_id TIPO_EMP.IDTIPOEMPLEADO%TYPE;
    v_tipo_empleado_desc TIPO_EMP.DESCRIPCION%TYPE;
BEGIN
   
    SELECT
    	E.IDTIPOEMPLEADO,
    	TE.DESCRIPCION
    INTO v_tipo_empleado_id,  
    	v_tipo_empleado_desc  
    FROM EMPLEADO E
    JOIN TIPO_EMP TE
    ON E.IDTIPOEMPLEADO = TE.IDTIPOEMPLEADO
    WHERE E.IDEMPLEADO = :NEW.IDEMPLEADO;

    -- Verificar si el tipo de empleado es 'OPERATIVO'.
    IF v_tipo_empleado_desc <> 'OPERATIVO'
    THEN
        RAISE_APPLICATION_ERROR(-20001,'ERROR LOGISTICO: Solo el personal OPERATIVO puede manejar ingresos/salidas de ordenes. Rol detectado: ' || v_tipo_empleado_desc);
    END IF;

END VALIDAR_OPERATIVO;
/


