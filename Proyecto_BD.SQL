--Creación de tablas. 
CREATE TABLE CEDIS (
    IDCEDIS INT,
    NOMBRE VARCHAR2(20) NOT NULL,
    DIRECCION VARCHAR2(100) NOT NULL,
    CONSTRAINT CEDISPK PRIMARY KEY (IDCEDIS)
);

CREATE TABLE PRODUCTO (
    SKU INT,
    NOMBRE VARCHAR2(250) NOT NULL,
    DESCRIPCION VARCHAR2(250) NOT NULL,
    PESO NUMBER(10,2) NOT NULL,
    ANCHO NUMBER(10,2) NOT NULL,
    LARGO NUMBER(10,2) NOT NULL,
    CONSTRAINT PRODUCTOPK PRIMARY KEY (SKU),
    CONSTRAINT CHK_PRODUCTO_DIMENSIONES CHECK (PESO > 0 AND ANCHO > 0 AND LARGO > 0)
);


CREATE TABLE TIPO_PRODUCT (
    SKU INT,
    TIPOPRODUCTO VARCHAR2(20) NOT NULL,
    CONSTRAINT TIPOPROPK PRIMARY KEY (SKU),
    CONSTRAINT TIPOPRO_FK FOREIGN KEY (SKU) REFERENCES PRODUCTO(SKU),
    CONSTRAINT TIPOPRODCK CHECK (TIPOPRODUCTO IN ('ELECTRONICA', 'ROPA', 'LIBROS', 'BELLEZA'))
);


CREATE TABLE CLIENTE (
    IDCLIENTE INT,
    RFC VARCHAR2(13) NOT NULL,
    NOMBRE VARCHAR2(29) NOT NULL, 
    CONSTRAINT CLIENTEPK PRIMARY KEY (IDCLIENTE),
    CONSTRAINT RFC_UNI UNIQUE (RFC)
);



CREATE TABLE CLIENTE_EMAIL(
    IDCLIENTE INT,
    EMAIL VARCHAR2(25) NOT NULL,
    CONSTRAINT CLIEMPK PRIMARY KEY (IDCLIENTE, EMAIL),
    CONSTRAINT CLIEMFK FOREIGN KEY (IDCLIENTE) REFERENCES CLIENTE(IDCLIENTE),
    CONSTRAINT CLIEMUNI UNIQUE (EMAIL),
    CONSTRAINT CLIEMEMAIL_CK CHECK (EMAIL LIKE '%@%')
);


CREATE TABLE CLIENTE_TELE(
    IDCLIENTE INT,
    TELEFONO VARCHAR2(15) NOT NULL,
    CONSTRAINT CLITELPK PRIMARY KEY (IDCLIENTE, TELEFONO),
    CONSTRAINT CLITELFK FOREIGN KEY (IDCLIENTE) REFERENCES CLIENTE(IDCLIENTE),
    CONSTRAINT CLITELUNI UNIQUE (TELEFONO)
);


CREATE TABLE PROVEE(
    IDPROVEE INT, 
    IDCLIENTE INT NOT NULL, 
    SKU INT NOT NULL, 
    FECHAHORA DATE NOT NULL, 
    CONSTRAINT PROVPK PRIMARY KEY (IDPROVEE),
    CONSTRAINT PCLIFK FOREIGN KEY (IDCLIENTE) REFERENCES CLIENTE (IDCLIENTE),
    CONSTRAINT PPRODFK FOREIGN KEY (SKU) REFERENCES PRODUCTO (SKU),
    CONSTRAINT PUNIQUE UNIQUE (IDCLIENTE, SKU, FECHAHORA)
);


CREATE TABLE ESTANTE (
    IDESTANTE INT, 
    UBICACION VARCHAR2 (50) NOT NULL,
    IDCEDIS INT NOT NULL,
    CONSTRAINT ESTPK PRIMARY KEY (IDESTANTE),
    CONSTRAINT ESTFK FOREIGN KEY (IDCEDIS) REFERENCES CEDIS (IDCEDIS)
);


CREATE TABLE POSICION (
    IDPOSICIONSUB INT,
    IDESTANTE INT NOT NULL,
    NOPOS INT NOT NULL, 
    SECCION VARCHAR2(4) NOT NULL,
    NIVEL VARCHAR2(4) NOT NULL, 
    IDPROVEE INT NOT NULL, 
    CONSTRAINT PPOSPK PRIMARY KEY (IDPOSICIONSUB),
    CONSTRAINT PESTFK FOREIGN KEY (IDESTANTE) REFERENCES ESTANTE (IDESTANTE),
    CONSTRAINT PPEEFK FOREIGN KEY (IDPROVEE) REFERENCES PROVEE (IDPROVEE),
    CONSTRAINT POUNI UNIQUE (IDESTANTE, NOPOS)
);


CREATE TABLE TIPO_ORDEN (
    IDTIPOORDEN INT, 
    DESCRIPCION VARCHAR2(50) NOT NULL, 
    CONSTRAINT TIORPK PRIMARY KEY (IDTIPOORDEN),
    CONSTRAINT TOCHECK CHECK (DESCRIPCION IN ('INGRESO', 'SALIDA'))
);


CREATE TABLE TIPO_EMP(
    IDTIPOEMPLEADO INT, 
    DESCRIPCION VARCHAR2(50) NOT NULL,
    CONSTRAINT TIEMPK PRIMARY KEY (IDTIPOEMPLEADO),
    CONSTRAINT TIEMUNI UNIQUE (DESCRIPCION),
    CONSTRAINT DESCH CHECK (DESCRIPCION IN ('ADMINISTRATIVO', 'OPERATIVO', 'AUXILIAR'))
);


CREATE TABLE EMPLEADO (
    IDEMPLEADO INT, 
    RFC VARCHAR2(13) NOT NULL, 
    HORARIOLABORAL VARCHAR2 (50) NOT NULL,
    PILA VARCHAR2(15) NOT NULL, 
    APPAT VARCHAR2(15) NOT NULL, 
    APMAT VARCHAR2(15) NULL, 
    DIRECCION VARCHAR2 (100) NOT NULL, 
    IDEMPSUP INT NULL,
    IDTIPOEMPLEADO INT NOT NULL, 
    IDCEDIS INT NOT NULL, 
    CONSTRAINT EMPPK PRIMARY KEY (IDEMPLEADO),
    CONSTRAINT SUPFK FOREIGN KEY (IDEMPSUP) REFERENCES EMPLEADO (IDEMPLEADO),
    CONSTRAINT TEMFK FOREIGN KEY (IDTIPOEMPLEADO) REFERENCES TIPO_EMP (IDTIPOEMPLEADO),
    CONSTRAINT CEDFK FOREIGN KEY (IDCEDIS) REFERENCES CEDIS (IDCEDIS),
    CONSTRAINT ERFCUNI UNIQUE (RFC)
);


CREATE TABLE EMP_EMAIL(
    IDEMPLEADO INT, 
    EMAIL VARCHAR2 (25) NOT NULL,
    CONSTRAINT EMEPK PRIMARY KEY (IDEMPLEADO, EMAIL),
    CONSTRAINT EMEFK FOREIGN KEY (IDEMPLEADO) REFERENCES EMPLEADO (IDEMPLEADO),
    CONSTRAINT EMEUNI UNIQUE (EMAIL),
    CONSTRAINT EMECH CHECK (EMAIL LIKE '%@%')
);


CREATE TABLE EMP_TEL(
    IDEMPLEADO INT, 
    TELEFONO VARCHAR2(15) NOT NULL, 
    CONSTRAINT EMTEPK PRIMARY KEY (IDEMPLEADO, TELEFONO),
    CONSTRAINT EMTEFK FOREIGN KEY (IDEMPLEADO) REFERENCES EMPLEADO (IDEMPLEADO),
    CONSTRAINT EMTEUNI UNIQUE (TELEFONO)
);


CREATE TABLE ORDEN (
    FOLIO INT, 
    FECHACREACION DATE NOT NULL, 
    ESTADO VARCHAR2(15) NOT NULL, 
    IDTIPOORDEN INT NOT NULL, 
    IDEMPLEADO INT NOT NULL,
    IDPROVEE INT NOT NULL, 
    IDCEDIS INT NOT NULL, 
    CANTIDAD INT NOT NULL,
    CONSTRAINT ORDPK PRIMARY KEY (FOLIO),
    CONSTRAINT TORDFK FOREIGN KEY (IDTIPOORDEN) REFERENCES TIPO_ORDEN (IDTIPOORDEN),
    CONSTRAINT OEMPFK FOREIGN KEY (IDEMPLEADO) REFERENCES EMPLEADO (IDEMPLEADO),
    CONSTRAINT OPROFK FOREIGN KEY (IDPROVEE) REFERENCES PROVEE (IDPROVEE),
    CONSTRAINT OCEDFK FOREIGN KEY (IDCEDIS) REFERENCES CEDIS (IDCEDIS),
    CONSTRAINT ESTCH CHECK (ESTADO IN ('PENDIENTE', 'EN PROCESO', 'COMPLETADA')),
    CONSTRAINT OCANTIDAD CHECK (CANTIDAD > 0) 
);


CREATE TABLE AUDITORIA (
    IDAUDITORIA INT, 
    IDEMPLEADO INT NOT NULL, 
    FOLIO INT NOT NULL, 
    FECHAHORA DATE NOT NULL,
    ESTADO VARCHAR2(15) NOT NULL, 
    CONSTRAINT AUDIPK PRIMARY KEY (IDAUDITORIA),
    CONSTRAINT AFOFK FOREIGN KEY (FOLIO) REFERENCES ORDEN (FOLIO),
    CONSTRAINT AEMPFK FOREIGN KEY (IDEMPLEADO) REFERENCES EMPLEADO (IDEMPLEADO),
    CONSTRAINT AUNIQUE UNIQUE (IDEMPLEADO, FOLIO, FECHAHORA),
    CONSTRAINT AESTCH CHECK (ESTADO IN ('CONFORME', 'NO CONFORME'))
);
    
    
